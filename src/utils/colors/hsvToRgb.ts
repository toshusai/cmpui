import { HSV, RGB } from "./types";

/**
 * Reference:
 * - Algorithm from: https://en.wikipedia.org/wiki/HSL_and_HSV#HSV_to_RGB
 */
export function hsvToRgb(hsv: HSV): RGB {
  let r = 0;
  let g = 0;
  let b = 0;

  const h = hsv.h;
  const s = hsv.s;
  const v = hsv.v;

  const i = Math.floor(h / 60);
  const f = h / 60 - i;
  const p = v * (1 - s);
  const q = v * (1 - s * f);
  const t = v * (1 - s * (1 - f));

  switch (i % 6) {
    case 0:
      r = v;
      g = t;
      b = p;
      break;
    case 1:
      r = q;
      g = v;
      b = p;
      break;
    case 2:
      r = p;
      g = v;
      b = t;
      break;
    case 3:
      r = p;
      g = q;
      b = v;
      break;
    case 4:
      r = t;
      g = p;
      b = v;
      break;
    case 5:
      r = v;
      g = p;
      b = q;
      break;
  }

  return {
    r: Math.round(r * 255),
    g: Math.round(g * 255),
    b: Math.round(b * 255),
  };
}
